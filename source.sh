#!/bin/bash

###############################################################################
src () { [ ! -z "$1" ] && . "$1"; PSD="$(dirname "${BASH_SOURCE[0]}")"; }; src
###############################################################################

for fct in $(ls -1 "$PSD/fct_"* 2>/dev/null); do
    src "$fct"
done

src "$PSD/colors.sh"
src "$PSD/alias.sh"

